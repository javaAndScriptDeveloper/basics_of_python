# Тестові значення:
# 1) Периметр прямокутника: (5, 3), (10, 7), (2.5, 4)
# 2) Факторіал: 5, 8
# 3) Цельсій → Фаренгейт: 0, 100, 37
# 4) Кілометри → метри: 1, 5.5, 0.25
# 5) Години → хвилини: 2, 5.5, 0.25
# 6) Середнє арифметичне: [15, 25, 35], [100, 200, 300]
# 7) Площа квадрата: 5, 12, 7
# 8) Найбільше з трьох: (3, 17, 5), (10, 2, 8), (42, 15, 38)
# 9) Дюйми → сантиметри: 1, 10, 5
# 10) Кількість голосних: "hello world", "education", "beautiful day"

def count_vowels(text):
    """
    Функція для підрахунку кількості голосних літер у рядку.
    Голосні англійського алфавіту: a, e, i, o, u.
    """
    # Рядок з усіма голосними літерами (як малі, так і великі)
    vowels = "aeiouAEIOU"
    count = 0
    
    # Перевіряємо кожен символ у переданому рядку
    for char in text:
        if char in vowels:
            count += 1
            
    return count

# Тестові значення для варіанту 10
test_strings = ["hello world", "education", "beautiful day"]

print("--- Результати підрахунку голосних літер ---")

# Виклик функції для кожного тестового значення та виведення результату
for s in test_strings:
    vowel_count = count_vowels(s)
    print(f'У рядку "{s}" знайдено голосних: {vowel_count}')