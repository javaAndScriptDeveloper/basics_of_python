# Виведіть результат кожного виклику за допомогою print()
#
# Тестові значення:
# 3) Цельсій → Фаренгейт: 0, 100, 37

# Визначення функції для переведення температури
def cels_to_faren(celsius):
    faren = (celsius * 9/5) + 32
    return faren

test_values = [0, 100, 37]

# Виклик функції для кожного значення та виведення результату
for temp in test_values:
    result = cels_to_faren(temp)
    print(result)