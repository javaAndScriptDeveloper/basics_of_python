# Визначаємо функцію для обчислення середнього арифметичного
def calculate_average(numbers_list):
    """
    Функція обчислює середнє арифметичне списку чисел.
    
    Аргументи:
    numbers_list (list): список чисел для обчислення
    
    Повертає:
    float: середнє арифметичне значення, або 0, якщо список порожній.
    """
    # Перевірка на порожній список, щоб уникнути ділення на нуль
    if not numbers_list:
        return 0
        
    total_sum = sum(numbers_list) # Знаходимо суму всіх чисел
    count = len(numbers_list)     # Знаходимо кількість чисел
    
    return total_sum / count      # Повертаємо середнє арифметичне

# Основна частина програми (тестування функції)
def main():
    # Тестові значення з умови
    test_case_1 = [15, 25, 35]
    test_case_2 = [100, 200, 300]
    
    # Виклик функції та виведення результатів
    result_1 = calculate_average(test_case_1)
    print(f"Середнє арифметичне для списку {test_case_1} дорівнює: {result_1}")
    
    result_2 = calculate_average(test_case_2)
    print(f"Середнє арифметичне для списку {test_case_2} дорівнює: {result_2}")

# Запуск основної програми
if __name__ == "__main__":
    main()