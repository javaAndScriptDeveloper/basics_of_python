# Функцію необхідно викликати для кількох різних значень та вивести
# результати на екран.
# Індивідуальні завдання
# 2) Реалізувати функцію для обчислення факторіала числа.

def factorial(n):
    if n == 0:
        return 1

    result = 1
    for i in range(1, n + 1):
        result = result * i
    return result


print(factorial(5))
print(factorial(8))
