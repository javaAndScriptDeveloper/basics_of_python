# Введення трьох чисел
x = float(input("\nВведіть перше число для обчислення середнього: "))
y = float(input("Введіть друге число для обчислення середнього: "))
z = float(input("Введіть третє число для обчислення середнього: "))

# Обчислення середнього арифметичного
average = (x + y + z) / 3

# Виведення результату
print("Середнє арифметичне трьох чисел:", average)
