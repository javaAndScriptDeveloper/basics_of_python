
# Введення двох чисел
a = float(input("Введіть перше число: "))
b = float(input("Введіть друге число: "))

# Обчислення суми, різниці та добутку
sum_result = a + b
diff_result = a - b
mult_result = a * b

# Виведення результатів
print("\nРезультати загального завдання:")
print("Сума чисел:", sum_result)
print("Різниця чисел:", diff_result)
print("Добуток чисел:", mult_result)

# Перевірка ділення на нуль
if b != 0:
    div_result = a / b
    print("Частка чисел:", div_result)
else:
    print("Ділення на нуль неможливе!")

