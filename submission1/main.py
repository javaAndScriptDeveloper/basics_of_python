# Загальне завдання та Індивідуальне завдання (Варіант 4: Програма для ділення двох чисел)

def main():
    print("Вітаю!")
    
    a = float(input("Введіть перше число: "))
    b = float(input("Введіть друге число: "))

    print(f"Сума: {a + b}")
    print(f"Різниця: {a - b}")
    print(f"Добуток: {a * b}")

    # Індивідуальне завдання
    if b != 0:
        result = a / b
        print(f"Результат ділення: {result}")
    else:
        print("Ділення на нуль неможливе!")

if __name__ == "__main__":
    main()
