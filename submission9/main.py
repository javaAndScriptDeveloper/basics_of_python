import re
import sys

def main():
    # Зчитуємо всі дані, які bash-скрипт передає через стандартне введення
    text = sys.stdin.read().strip()
    
    if not text:
        return

    # Шаблон [^\w\s] шукає все, що НЕ є буквою, цифрою (\w) або пробілом (\s)
    pattern = r'[^\w\s]'
    
    # Видаляємо всі знайдені небажані символи (замінюємо на порожній рядок)
    cleaned_text = re.sub(pattern, '', text)
    
    # Виводимо очищений текст (саме це очікує перевіряючий скрипт)
    print(cleaned_text)

if __name__ == "__main__":
    main()