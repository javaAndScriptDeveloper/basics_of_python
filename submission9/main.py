# Тема: Використання регулярних виразів для пошуку та заміни в рядках

import re

def main():
    # Введення початкових даних (тексту)
    try:
        text = input("Введіть текст: ")
    except EOFError:
        return
        
    # Шаблон регулярного виразу:
    # \b - межа слова (початок слова)
    # [A-ZА-ЯІЇЄҐ] - велика літера (латиниця або українська кирилиця)
    # \w* - будь-яка кількість літер/цифр, що утворюють решту слова
    pattern = r'\b[A-ZА-ЯІЇЄҐ]\w*\b'
    
    # Використання функції findall для пошуку всіх збігів у рядку
    capitalized_words = re.findall(pattern, text)
    
    # Перевірка наявності знайдених слів та виведення результату
    if capitalized_words:
        print("Знайдені слова:", capitalized_words)
    else:
        print("Не знайдено жодного слова, що починається з великої літери.")

if __name__ == "__main__":
    main()