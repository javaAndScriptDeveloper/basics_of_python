# Лабораторна робота 9: Регулярні вирази

# Реалізуйте завдання тут
import re

# Ввід даних з клавіатури
ip = input("Введіть IP-адресу: ")

# Регулярний вираз для перевірки IPv4
pattern = r"^(\d{1,3}\.){3}\d{1,3}$"

# Перевірка базового формату
if re.match(pattern, ip):
    parts = ip.split(".")
    
    # перевірка діапазону кожної частини (0–255)
    valid = True
    for part in parts:
        if not (0 <= int(part) <= 255):
            valid = False
            break

    if valid:
        print("IP-адреса валідна")
    else:
        print("IP-адреса НЕвалідна (значення повинні бути 0–255)")
else:
    print("IP-адреса НЕвалідна (неправильний формат)")