print("Перевірка наявності ключа у словнику з обробкою винятку KeyError.")

# Налаштування логування для запису помилок у файл
logging.basicConfig(filename=error_file, level=logging.ERROR, 
                    format='%(asctime)s - %(levelname)s - %(message)s',
                    encoding='utf-8')

# Запитуємо у користувача ключ, який він хоче знайти
key_to_find = input("\nВведіть ключ для пошуку у словнику (наприклад, 'name', 'age', 'phone'): ")

try:
    # Спроба доступу до значення за введеним ключем 
    value = data[key_to_find]
    
except KeyError as e:
    # Обробка ситуації, коли ключ відсутній у словнику [cite: 5086, 5151]
    error_msg = f"Помилка KeyError: Ключ '{key_to_find}' не знайдено у словнику."
    print(f"\n❌ {error_msg}")
    
    # Запис інформації про виняток у лог-файл 
    logging.error(error_msg)
    print(f"Інформацію про помилку записано у файл '{error_file}'.")

else:
    # Виконується, якщо винятків не виникло (ключ знайдено) 
    print(f"\n✅ Ключ знайдено! Значення: {value}")

finally:
    # Виконується завжди, незалежно від результату 
    print("\n[Завершення перевірки словника]")
