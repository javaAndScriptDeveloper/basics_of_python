# Варіант 4: Числа Фібоначчі

# Введення граничного значення
limit = int(input("Введіть число, до якого виводити послідовність Фібоначчі: "))

if limit < 0:
    print("Будь ласка, введіть додатне число.")
else:
    print(f"Послідовність Фібоначчі до {limit}:")
    
    a, b = 0, 1
    fib_sequence = []

    # Використання циклу while для генерації послідовності
    while a <= limit:
        fib_sequence.append(str(a))
        a, b = b, a + b

    # Виводимо всі числа через пробіл
    print(" ".join(fib_sequence))

print("\nРоботу програми завершено.")