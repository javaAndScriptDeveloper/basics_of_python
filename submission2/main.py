# Знайти найбільше число серед трьох введених.

# 1. Введення трьох чисел з клавіатури
num1 = float(input("Введіть перше число: "))
num2 = float(input("Введіть друге число: "))
num3 = float(input("Введіть третє число: "))

# 2. Обробка даних: пошук найбільшого числа
# Перевіряємо, чи є перше число найбільшим
if num1 >= num2 and num1 >= num3:
    max_num = num1
# Якщо перше не найбільше, перевіряємо друге
elif num2 >= num1 and num2 >= num3:
    max_num = num2
# Якщо ні перше, ні друге не є найбільшими, значить найбільше - третє
else:
    max_num = num3

# 3. Виведення результату
print(f"Найбільше число серед введених: {max_num}")