def print_geometric_progression(b1, q, n):
    """
    Обчислює та виводить n членів геометричної прогресії.
    """
    current_term = b1
    print(f"\nПерші {n} членів геометричної прогресії:")
    
    for i in range(n):
        # Виводимо поточний член. Використовуємо end=", " для виводу в один рядок
        print(current_term, end=", " if i < n - 1 else "\n")
        
        # Обчислюємо наступний член, множачи поточний на знаменник (q)
        current_term *= q

# Отримуємо дані від користувача з перевіркою на правильність введення
try:
    b1 = float(input("Введіть перший член прогресії (b1): "))
    q = float(input("Введіть знаменник прогресії (q): "))
    n = int(input("Введіть кількість членів прогресії (n): "))

    if n <= 0:
        print("Помилка: кількість членів (n) має бути цілим числом, більшим за нуль.")
    else:
        print_geometric_progression(b1, q, n)

except ValueError:
    print("Помилка: будь ласка, введіть коректні числові значення.")